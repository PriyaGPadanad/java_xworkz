public class Animal {
    public Animal() {
        System.out.println("Animal is superclass");
    }
    public void eat() {
        System.out.println("Animal eating");
    }
    public void sleep() {
        System.out.println("Animal sleeping");
    }
    public void move(){
        System.out.println("Animal moved");
    }
    public void breathe(){
        System.out.println("Animal breathed");
    }

}
